from __future__ import annotations

import argparse
import csv
import random
from pathlib import Path

import networkx as nx

from dfl_secure_aggregation.network import graph


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Create topology JSON files from a sweep topologies.csv manifest."
    )
    parser.add_argument(
        "--topologies-csv",
        required=True,
        type=Path,
        help="Path to the topologies.csv file generated by generate_sweep_configs.py.",
    )
    parser.add_argument("--nodes", type=int, default=128)
    parser.add_argument("--small-world-k", type=int, default=4)
    parser.add_argument("--small-world-beta", type=float, default=0.1)
    parser.add_argument("--scale-free-m", type=int, default=10)
    parser.add_argument("--edge-density", type=float, default=1.0)
    parser.add_argument(
        "--include-pending",
        action="store_true",
        help="Also create topologies whose manifest status is not active.",
    )
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="Recreate topology files even if they already exist.",
    )
    return parser.parse_args()


def read_topology_rows(path: Path) -> list[dict]:
    with path.open(newline="") as f:
        return list(csv.DictReader(f))


def row_int(row: dict, key: str, default: int) -> int:
    value = row.get(key)
    if value in (None, ""):
        return default
    return int(value)


def row_float(row: dict, key: str, default: float) -> float:
    value = row.get(key)
    if value in (None, ""):
        return default
    return float(value)


def num_malicious(num_nodes: int, malicious_proportion: float) -> int:
    return int(num_nodes * malicious_proportion)


def choose_random_malicious(num_nodes: int, malicious_count: int, seed: int) -> list[int]:
    rng = random.Random(seed)
    return rng.sample(range(num_nodes), malicious_count)


def choose_high_degree_malicious(topology: graph.Topology, malicious_count: int) -> list[int]:
    sorted_nodes = sorted(topology.nodes, key=lambda node: topology.degree(node), reverse=True)
    return sorted_nodes[:malicious_count]


def choose_small_world_strategic_malicious(
    topology: graph.Topology,
    malicious_count: int,
    num_nodes: int,
    small_world_k: int,
) -> list[int]:
    malicious = []
    malicious_set = set()
    rewired_edges = set()

    # Preserve the old paper logic: target endpoints of long-range rewired edges.
    for node in topology.nodes:
        for neighbor in topology.get_neighbors(node):
            edge = tuple(sorted((node, neighbor)))
            if edge in rewired_edges:
                continue
            if abs(node % num_nodes - neighbor % num_nodes) > small_world_k:
                rewired_edges.add(edge)
                if node not in malicious_set:
                    malicious.append(node)
                    malicious_set.add(node)
                break
        if len(malicious) >= malicious_count:
            return malicious[:malicious_count]

    # If the requested Byzantine proportion needs more nodes than rewired endpoints
    # provide, fill from the highest-degree remaining nodes.
    for node in choose_high_degree_malicious(topology, num_nodes):
        if node not in malicious_set:
            malicious.append(node)
            malicious_set.add(node)
        if len(malicious) >= malicious_count:
            break

    return malicious[:malicious_count]


def clear_malicious_flags(topology: graph.Topology) -> None:
    for node in topology.nodes:
        topology.nodes[node]["malicious"] = False


def create_base_topology(
    topology_name: str,
    num_nodes: int,
    small_world_k: int,
    small_world_beta: float,
    scale_free_m: int,
    edge_density: float,
    seed: int,
) -> graph.Topology:
    topology = graph.Topology()
    if topology_name == "small-world":
        topology.network_type = "small_world"
        base_graph = nx.watts_strogatz_graph(
            num_nodes,
            small_world_k,
            small_world_beta,
            seed=seed,
        )
        topology.load_from_graph(base_graph)
    elif topology_name == "scale-free":
        topology.network_type = "scale_free"
        base_graph = nx.barabasi_albert_graph(
            n=num_nodes,
            m=scale_free_m,
            seed=seed,
        )
        topology.load_from_graph(base_graph)
    elif topology_name == "random":
        random.seed(seed)
        topology.create_random_graph(num_nodes, edge_density, malicious_nodes=[])
    else:
        raise ValueError(f"Unsupported topology: {topology_name}")
    clear_malicious_flags(topology)
    return topology


def choose_malicious_nodes(
    topology: graph.Topology,
    topology_name: str,
    placement: str,
    malicious_count: int,
    num_nodes: int,
    seed: int,
    small_world_k: int,
) -> list[int]:
    if malicious_count == 0:
        return []
    if placement == "random":
        return choose_random_malicious(num_nodes, malicious_count, seed)
    if placement != "strategic":
        raise ValueError(f"Unsupported placement: {placement}")
    if topology_name == "small-world":
        return choose_small_world_strategic_malicious(
            topology=topology,
            malicious_count=malicious_count,
            num_nodes=num_nodes,
            small_world_k=small_world_k,
        )
    return choose_high_degree_malicious(topology, malicious_count)


def create_topology_from_row(row: dict, args: argparse.Namespace) -> Path:
    output_path = Path(row["topology_file"])
    if output_path.exists() and not args.overwrite:
        return output_path

    topology_name = row["topology"]
    placement = row["placement"]
    seed = int(row["seed"])
    num_nodes = row_int(row, "nodes", args.nodes)
    small_world_k = row_int(row, "small_world_k", args.small_world_k)
    small_world_beta = row_float(row, "small_world_beta", args.small_world_beta)
    scale_free_m = row_int(row, "scale_free_m", args.scale_free_m)
    edge_density = row_float(row, "edge_density", args.edge_density)
    malicious_proportion = float(row["malicious_proportion"])
    malicious_count = num_malicious(num_nodes, malicious_proportion)

    random.seed(seed)

    topology = create_base_topology(
        topology_name=topology_name,
        num_nodes=num_nodes,
        small_world_k=small_world_k,
        small_world_beta=small_world_beta,
        scale_free_m=scale_free_m,
        edge_density=edge_density,
        seed=seed,
    )
    malicious_nodes = choose_malicious_nodes(
        topology=topology,
        topology_name=topology_name,
        placement=placement,
        malicious_count=malicious_count,
        num_nodes=num_nodes,
        seed=seed,
        small_world_k=small_world_k,
    )
    topology.set_malicous(malicious_nodes)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    topology.save(output_path)
    return output_path


def main() -> None:
    args = parse_args()
    rows = read_topology_rows(args.topologies_csv)

    created = 0
    skipped = 0
    for row in rows:
        if row.get("status") != "active" and not args.include_pending:
            skipped += 1
            continue
        output_path = Path(row["topology_file"])
        existed = output_path.exists()
        create_topology_from_row(row, args)
        if existed and not args.overwrite:
            skipped += 1
        else:
            created += 1

    print(f"Created topology files: {created}")
    print(f"Skipped topology rows: {skipped}")


if __name__ == "__main__":
    main()
